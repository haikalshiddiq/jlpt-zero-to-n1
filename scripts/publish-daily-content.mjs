import fs from 'node:fs';
import path from 'node:path';

const root = process.cwd();
const queuePath = path.join(root, 'automation/daily-content-queue/queue.json');
const publishedPath = path.join(root, 'src/data/daily-additions.json');

const readJson = (file, fallback) => {
  if (!fs.existsSync(file)) return fallback;
  const raw = fs.readFileSync(file, 'utf8').trim();
  return raw ? JSON.parse(raw) : fallback;
};

const queue = readJson(queuePath, []);
const published = readJson(publishedPath, []);
const publishedIds = new Set(published.map((item) => item.id));
const today = process.env.CONTENT_DATE || new Date().toISOString().slice(0, 10);

let next = queue.find((item) => !publishedIds.has(item.id) && (!item.dueDate || item.dueDate <= today));
if (!next) next = queue.find((item) => !publishedIds.has(item.id));

if (!next) {
  const sequence = published.length + 1;
  next = {
    id: `auto-${today}-${sequence}`,
    dueDate: today,
    title: `Daily JLPT Growth Pack ${sequence}`,
    level: sequence < 30 ? 'N5' : sequence < 90 ? 'N4' : sequence < 180 ? 'N3' : sequence < 270 ? 'N2' : 'N1',
    focus: ['vocabulary', 'kanji', 'grammar', 'reading', 'listening', 'review'][sequence % 6],
    objective: 'Extend the curriculum with a compact study mission generated by the daily CI/CD pipeline.',
    activity: 'Complete one flashcard, one sentence, one recall check, and one reflection note.',
    microQuiz: [
      { q: 'What is the focus of this daily pack?', a: 'active recall and steady JLPT progress' },
      { q: 'What should be done after answering?', a: 'review mistakes and schedule repetition' }
    ],
    status: 'generated'
  };
}

const enriched = {
  ...next,
  publishedAt: new Date().toISOString(),
  pipeline: 'daily-content-pipeline'
};

published.unshift(enriched);
fs.writeFileSync(publishedPath, `${JSON.stringify(published, null, 2)}\n`);
console.log(`Published daily content: ${enriched.id} — ${enriched.title}`);
